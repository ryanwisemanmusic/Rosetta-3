pub fn exitConditionName(cond: ExitCondition) []const u8 {
    return switch (cond) {
        .always => "jmp",
        .carry => "jb",
        .not_carry => "jnb",
        .zero => "je",
        .not_zero => "jne",
        .below => "jb",
        .above_equal => "jae",
        .signed_less => "jl",
        .signed_greater_equal => "jge",
        .signed_greater => "jg",
        .signed_less_equal => "jle",
        .overflow => "jo",
        .not_overflow => "jno",
        .parity => "jp",
        .not_parity => "jnp",
        .ecx_zero => "jecxz",
        _ => "j??",
    };
}

