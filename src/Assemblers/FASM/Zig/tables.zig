fn null_op(_: u8) []const u8 {
    return "op";
}

test "register name lookup" {
    try std.testing.expectEqualStrings("eax", registerName(16));
    try std.testing.expectEqualStrings("rax", registerName(24));
    try std.testing.expectEqualStrings("xmm0", registerName(64));
    try std.testing.expectEqualStrings("ymm0", registerName(80));
}

test "exit condition names" {
    try std.testing.expectEqualStrings("je", exitConditionName(.zero));
    try std.testing.expectEqualStrings("jmp", exitConditionName(.always));
}
